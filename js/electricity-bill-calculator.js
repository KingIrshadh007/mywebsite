// =============================
// Electricity Bill Calculator
// =============================

let billData = null;

// Calculate Bill
function calculateElectricityBill() {

    const state = document.getElementById("state").value;

    let units = Number(document.getElementById("units").value);
    let freeUnits = Number(document.getElementById("freeUnits").value);
    let rate = Number(document.getElementById("rate").value);
    let fixedCharge = Number(document.getElementById("fixedCharge").value);

    if (units <= 0 || rate <= 0) {
        alert("Please enter valid units and rate.");
        return;
    }

    let chargeableUnits = units - freeUnits;

    if (chargeableUnits < 0) {
        chargeableUnits = 0;
    }

    let energyCharge = chargeableUnits * rate;

    let totalAmount = energyCharge + fixedCharge;

    // Save data for PDF
    billData = {
        state,
        units,
        freeUnits,
        chargeableUnits,
        rate,
        fixedCharge,
        totalAmount
    };

    document.getElementById("billResult").innerHTML = `

        <div class="bill-header">
            ⚡ Electricity Usage Report
        </div>

        <div class="bill-row">
            <span>State</span>
            <span>${state}</span>
        </div>

        <div class="bill-row">
            <span>Total Units</span>
            <span>${units}</span>
        </div>

        <div class="bill-row">
            <span>Free Units</span>
            <span>${freeUnits}</span>
        </div>

        <div class="bill-row">
            <span>Chargeable Units</span>
            <span>${chargeableUnits}</span>
        </div>

        <div class="bill-row">
            <span>Rate / Unit</span>
            <span>₹${rate}</span>
        </div>

        <div class="bill-row">
            <span>Fixed Charge</span>
            <span>₹${fixedCharge}</span>
        </div>

        <div class="bill-total">
            ₹${totalAmount.toFixed(2)}
        </div>

    `;
}


// =============================
// Download Beautiful PDF
// =============================

function downloadBillPDF() {

    if (!billData) {
        alert("Please calculate the bill first.");
        return;
    }

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();

    const now = new Date();

    // Outer Border
    doc.setDrawColor(30, 144, 255);
    doc.setLineWidth(2);
    doc.roundedRect(8, 8, 194, 280, 5, 5);

    // Header Background
    doc.setFillColor(30, 144, 255);
    doc.roundedRect(8, 8, 194, 30, 5, 5, "F");

    // Title
    doc.setTextColor(255,255,255);
    doc.setFontSize(22);
    doc.text("Electricity Usage Report", 48, 27);

    // Date
    doc.setTextColor(100);
    doc.setFontSize(10);

    doc.text(
        "Generated on : " +
        now.toLocaleDateString() +
        "  " +
        now.toLocaleTimeString(),
        15,
        48
    );

    // Details Card
    doc.setDrawColor(220);
    doc.setFillColor(248,249,250);
    doc.roundedRect(15,55,180,100,5,5,"FD");

    doc.setTextColor(0,0,0);
    doc.setFontSize(13);

    let y = 75;

    doc.text("State :",25,y);
    doc.text(billData.state,100,y);

    y += 15;
    doc.text("Units Consumed :",25,y);
    doc.text(String(billData.units),100,y);

    y += 15;
    doc.text("Free Units :",25,y);
    doc.text(String(billData.freeUnits),100,y);

    y += 15;
    doc.text("Chargeable Units :",25,y);
    doc.text(String(billData.chargeableUnits),100,y);

    y += 15;
    doc.text("Rate Per Unit :",25,y);
    doc.text("₹"+billData.rate,100,y);

    y += 15;
    doc.text("Fixed Charge :",25,y);
    doc.text("₹"+billData.fixedCharge,100,y);


    // Total Box
    doc.setFillColor(34,197,94);
    doc.roundedRect(30,180,150,35,8,8,"F");

    doc.setTextColor(255,255,255);
    doc.setFontSize(20);

    doc.text(
        "TOTAL AMOUNT : ₹" +
        billData.totalAmount.toFixed(2),
        50,
        202
    );


    // Thank You Card
    doc.setFillColor(59,130,246);
    doc.roundedRect(25,235,160,25,5,5,"F");

    doc.setFontSize(13);
    doc.setTextColor(255,255,255);

    doc.text(
        "Thank you for using Free Tools Hub",
        55,
        250
    );


    // Footer
    doc.setTextColor(120);
    doc.setFontSize(10);

    doc.text(
        "Generated by Free Tools Hub • www.freetoolshub.com",
        45,
        278
    );

    doc.save("Electricity_Usage_Report.pdf");
}



